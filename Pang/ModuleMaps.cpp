bool ModuleMaps::Start()
{
	ladderGraphics = App->textures->Load("./Image_Sources/Ladder.png");

	tile.h = tile.w = 8;

	return true;
}

update_status ModuleMaps::Update()
{	
	for (int h = 1; h <= 24; h++)
	{
		for (int w = 1; w <= 46; w++)
		{
			switch (map[h][w])
			{
				case 0:
				  { break; }
				case 1:
				{
						  tile.x = w*TILE;
						  tile.y = h*TILE;
						  App->renderer->DrawQuad(tile, 255, 255, 0, 255);
						  break;
				}
				case 2:
				{
					if (map[h][w - 1] != 2)
					{
						App->renderer->Blit(ladderGraphics, w * TILE, h * TILE, NULL);
						break;
					}
					
				}
			}
		}
	}
	return UPDATE_CONTINUE;
}

