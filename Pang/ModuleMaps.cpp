bool ModuleMaps::Start()
{
	ladderGraphics = App->textures->Load("./Image_Sources/Ladder.png");
	bricksGraphics = App->textures->Load("./Image_Sources/Bricks.png");

	tile.h = tile.w = 8;

	SolidBrickSection.h = SolidBrickSection.w = 8;
	SolidBrickSection.y = 0;

	return true;
}

update_status ModuleMaps::Update()
{	
	for (int h = 1; h <= 24; h++)
	{
		for (int w = 1; w <= 46; w++)
		{
			switch (map[h][w])
			{
				case 0:
				  { break; }
				case 1:
				{
						  tile.x = w*TILE;
						  tile.y = h*TILE;
						  if (lvl[App->backgroundPlay->currentLvl][h][w] != 1)
						  {
							  DrawGlassBrick(h, w);
						  }
						  else
						  {
							  DrawBrick(h, w);
						  }
						  break;
				}
				case 2:
				{
					if (map[h][w - 1] != 2)
					{
						/*if (map[h][w - 1] == 1 && map[h][w + 3] == 1)
						{
							tile.w = TILE * 3;
							tile.x = w*TILE;
							tile.y = h*TILE;
							App->renderer->DrawQuad(tile, 255, 255, 0, 255);
							tile.w = TILE;
						}*/
						App->renderer->Blit(ladderGraphics, w * TILE, h * TILE, NULL);
						break;
					}
					
				}
			}
		}
	}
	return UPDATE_CONTINUE;
}

bool ModuleMaps::CleanUp()
{
	App->textures->Unload(ladderGraphics);
	App->balls->Disable();
	App->boosts->Disable();
	return true;
}

void ModuleMaps::LoadMap(int nMap)
{
	App->player->boost = none;
	App->particles->Clear();
	App->boosts->Disable();
	App->balls->Disable();
	App->gun->Disable();
	App->gun->Enable();
	for (int h = 0; h < 26; h++)
	{
		for (int w = 0; w < 48; w++)
		{
			if (lvl[nMap][h][w] == 0 || lvl[nMap][h][w] == 2)
			{
				map[h][w] = lvl[nMap][h][w];
			}
			else
			{
				map[h][w] = 1;
			}
		}
	}
	App->boosts->Enable();
	App->balls->Enable();
	switch (nMap)
	{
	case 0:
	{
		App->balls->AddBall(50, 50, little, blue, 1);
		App->player->position.x = 1*TILE;
		App->player->position.y = 20*TILE;
		break;
	}
	case 1:
	{
		App->balls->AddBall(50, 50, huge, red, 1);
		App->player->position.x = 1 * TILE;
		App->player->position.y = 20 * TILE;
		App->audio->PlayMusic("./Sounds/1Mt_Fuji.ogg");
		break;
	}
	case 2:
	{
		App->balls->AddBall(20, 30, huge, blue, 1);
		App->balls->AddBall(200, 40, big, blue, 1);
		App->player->position.x = 1 * TILE;
		App->player->position.y = 20 * TILE;
		App->audio->PlayMusic("./Sounds/1Mt_Fuji.ogg");
		break;
	}
	case 3:
	{
		App->balls->AddBall(60, 50, huge, green, 1);
		App->player->position.x = 1 * TILE;
		App->player->position.y = 20 * TILE;
		App->audio->PlayMusic("./Sounds/1Mt_Fuji.ogg");
		break;
	}
	}
}

//Quan estas dins del joc, si apretes la "R" aniras a un mapa bo per probar el renderitzat
void ModuleMaps::DrawGlassBrick(int h, int w)
{
		if (map[h][w - 1] == 1 && map[h][w + 3] == 1)
		{
			App->renderer->DrawQuad(tile, 0, 255, 100 * lvl[App->backgroundPlay->currentLvl][h][w], 255);
		}
		if (map[h][w - 1] != 1 && map[h][w + 3] == 1)
		{
			App->renderer->DrawQuad(tile, 0, 200, 100 * lvl[App->backgroundPlay->currentLvl][h][w], 255);
		}
		if (map[h][w - 1] == 1 && map[h][w + 3] != 1)
		{
			App->renderer->DrawQuad(tile, 0, 150, 100 * lvl[App->backgroundPlay->currentLvl][h][w], 255);
		}
	
}

void ModuleMaps::DrawBrick(int h, int w)
{
	if ((lvl[App->backgroundPlay->currentLvl][h][w + 1] == 1 && lvl[App->backgroundPlay->currentLvl][h][w - 1] != 1) || w <=1 )
	{
		if (lvl[App->backgroundPlay->currentLvl][h][w + 2] != 1)
		{
			SolidBrickSection.x = 1 * TILE;
		}
		else
		{
			SolidBrickSection.x = 3 * TILE;
		}
	}
	else if(lvl[App->backgroundPlay->currentLvl][h][w + 1] != 1 && lvl[App->backgroundPlay->currentLvl][h][w - 1] == 1 || w>= 46)
	{
		if (lvl[App->backgroundPlay->currentLvl][h][w - 2] != 1)
		{
			SolidBrickSection.x = 2 * TILE;
		}
		else
		{
			SolidBrickSection.x = 6 * TILE;
		}
	}
	else if(lvl[App->backgroundPlay->currentLvl][h][w + 1] != 1 && lvl[App->backgroundPlay->currentLvl][h][w - 1] != 1)
	{
		SolidBrickSection.x = 0;
	}
	else
	{
		if (lvl[App->backgroundPlay->currentLvl][h][w + 2] != 1)
		{
			SolidBrickSection.x = 5 * TILE;
		}
		else
		{
			SolidBrickSection.x = 4 * TILE;
		}
	}
	App->renderer->Blit(bricksGraphics, w*TILE, h*TILE, &SolidBrickSection);
}