bool ModuleMaps::Start()
{
	ladderGraphics = App->textures->Load("./Image_Sources/Ladder.png");
	bricksGraphics = App->textures->Load("./Image_Sources/Bricks.png");
	timer = 0;
	tile.h = tile.w = 8;

	SolidBrickSection.h = SolidBrickSection.w = 8;
	SolidBrickSection.y = 0;
	

	return true;
}

update_status ModuleMaps::Update()
{	
	timer++;
	for (int h = 1; h <= 24; h++)
	{
		for (int w = 1; w <= 46; w++)
		{
			switch (map[h][w])
			{
				case 0:
				  { break; }
				case 1:
				{
						  tile.x = w*TILE;
						  tile.y = h*TILE;
						  if (lvl[App->scenePlay->currentLvl][h][w] != 1)
						  {
							  DrawGlassBrick(h, w);
						  }
						  else
						  {
							  DrawBrick(h, w);
						  }
						  break;
				}
				case 2:
				{
					if (map[h][w - 1] != 2)
					{
						/*if (map[h][w - 1] == 1 && map[h][w + 3] == 1)
						{
							tile.w = TILE * 3;
							tile.x = w*TILE;
							tile.y = h*TILE;
							App->render->DrawQuad(tile, 255, 255, 0, 255);
							tile.w = TILE;
						}*/
						App->render->Blit(ladderGraphics, w * TILE, h * TILE, NULL);
						break;
					}
					
				}
			}
		}
	}
	return UPDATE_CONTINUE;
}

bool ModuleMaps::CleanUp()
{
	App->textures->Unload(ladderGraphics);
	App->balls->Disable();
	App->boosts->Disable();
	return true;
}

